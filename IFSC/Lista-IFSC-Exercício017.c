#include <stdio.h>

int main() {
    int A, B, C, D;

    printf("Digite os valores A, B, C e D: ");
    scanf("%d %d %d %d", &A, &B, &C, &D);

    printf("A + B = %d\n", A + B);
    printf("A * C = %d\n", A * C);
    printf("C + D = %d\n", C + D);
    printf("B * C = %d\n", B * C);
    printf("C * D = %d\n", C * D);

    return 0;
}
